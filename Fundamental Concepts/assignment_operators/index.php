      //Same as
      $calc = 10 + 5;

      //Subtraction
      $calc -= 5;
      //Same as
      $calc = 10 - 5;

      //Multiplication
      $calc *= 5;
      //Same as
      $calc = 10 * 5;

      //Division
      $calc /= 5;
      //Same as
      $calc = 10 / 5;

      //Modulus
      $calc %= 5;
      //Same as
      $calc = 10 % 5;
